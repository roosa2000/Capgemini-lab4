package lab4;

public class Exercise1Per
{
	private String name;
	private float age;
	
	public Exercise1Per(String name,float age)
	{
		this.age=age;
		this.name=name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}

